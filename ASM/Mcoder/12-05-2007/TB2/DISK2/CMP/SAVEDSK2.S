;
	IF	LOADER=1
	DC.B	'INTRO',0
;	DC.B	'DEMO0',0
;	DC.B	'DEMO1',0
	DC.B	'MENU',0

	DC.B	'CYBERN',0
	DC.B	'DEFCON4',0
	DC.B	'EQUINOX',0
	DC.B	'HARD_3D',0
	DC.B	'SILVERS',0
	DC.B	'TMF',0
	DC.B	'TSB',0
	DC.B	'VOYAGERS',0
	DC.B	'WALLS',0
	ENDIF

	IF	LOADER=2
	DC.B	'ATM',0
	DC.B	'KNUCKLE3',0
	DC.B	'ORIGINAL',0
	DC.B	'OUTLAWS',0
	DC.B	'TBC',0
	DC.B	'UTS',0
	ENDIF

	DC.B	-1
	EVEN
