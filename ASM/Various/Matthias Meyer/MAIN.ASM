REGMOD:
 JSR READCHAR * Reg. ändern
 BEQ ERRO
 CMP.B #32,D0
 BEQ.S PCMOD
 JSR KLEINERBU
 CMP.B #'u',D0
 BEQ.S USPMOD
 CMP.B #'s',D0
 BEQ.S SSPMOD
 CMP.B #'0',D0
 BCS ERRO
 CMP.B #'8',D0
 BCC ERRO
 SUB.B #'0',D0
 MOVE.W D0,D2
 LSL.W #2,D0
 LEA REGIST,A0
 ADD.W D0,A0
 JSR READLONG
 MOVE.L D0,(A0)
 CMP.B #7,D2
 BEQ.S RMXT
 JSR READLONG
 MOVE.L D0,32(A0)
RMXT:
 RTS
PCMOD:
 JSR READLONG
 BTST #0,D0
 BNE ERRO * ungerade
 LEA PCOUNTER,A0
 MOVE.L D0,(A0)
 SUB.L #2,A0
 JSR READBINBYT  * hi-Byte Statusreg.
 AND.B #%10100111,D0
 MOVE.B D0,(A0)+
 JSR READBINBYT
 AND.B #%00011111,D0
 MOVE.B D0,(A0)
 RTS
USPMOD:
 LEA USERSTP,A0
 BRA.S STPMOD
SSPMOD:
 LEA SUPVSTP,A0
STPMOD:
 JSR MUSTREAD * "SP" überlesen
 JSR MUSTREAD
 JSR READLONG
 BTST #0,D0
 BNE ERRO * ungerade
 MOVE.L D0,(A0)
 RTS


MEMMOD:
 JSR READADR * Hexdump ändern
 MOVE.L D0,A0
 MOVE.L D0,A1
 MOVEQ #15,D1
MMODLP:
 JSR READBYTE
 MOVE.B D0,(A0)+
 DBF D1,MMODLP
* Hexdump neu ausgeben
 MOVE.L A1,A0
 JMP MEMOUT

KONTMOD:
 JSR READADR * Textauszug ändern
 MOVE.L D0,A0
 MOVEQ #3,D1
KOOLP:
 JSR READCHAR
 BEQ ERRO
 DBF D1,KOOLP
 MOVEQ #63,D1
KMODLP:
 JSR READCHAR
 BEQ ERRO
 CMP.B #'.',D0
 BEQ.S KMODXT
 MOVE.B D0,(A0)
KMODXT:
 ADDQ.L #1,A0
 DBF D1,KMODLP
 RTS

DISASSMOD:
 JSR READADR  * Disass. listing ändern
 BCLR #0,D0
 MOVE.L D0,A0
 MOVE.L D0,A2
 JSR READWORD
DISMODLP:
 MOVE.W D0,(A0)+    * alle Hexworte übernehmen
 JSR READCHAR
 BEQ.S DISMNEW
 CMP.B #32,D0
 BNE ERRO
 JSR READCHAR
 CMP.B #32,D0
 BEQ.S DISMNEW
 JSR READBACK
 JSR READWORD
 BRA.S DISMODLP
DISMNEW:
 MOVE.L A2,A0
 JSR DISASS   * nächste Listingzeile ausg.
 JSR CARRET
 JSR DISASS
 MOVEQ #10,D1
 MOVE.W CRSY,D2
 JSR RCRSET
 JMP NOPNTINP


OCCUPY:
 JSR READADR    * Speicherbereich mit Bytefolge ausfüllen
 MOVE.L D0,A0
 JSR READADR
 MOVE.L D0,A1
 CMP.L A1,A0    * Eadr mu× > Aadr
 BCC ERRO
 JSR READCHAR
 BNE.S OCDEF
 CLR.W D0       * Default Null
 BRA.S OPUBY
OCDEF:
 JSR READBACK
 JSR READBYTE
OPUBY:
 MOVE.W D0,-(SP)
 MOVEQ #2,D1
OBTLP:
 JSR READCHAR
 BEQ.S OBXT
 JSR READBACK
 JSR READBYTE
 MOVE.W D0,-(SP)
 ADD.W #2,D1
 BRA.S OBTLP
OBXT:
 MOVE.W D1,D2
OMEMLP:
 MOVE.W -2(A7,D2.W),D0
 SUB.W #2,D2
 BNE.S NNEWS
 MOVE.W D1,D2
NNEWS:
 MOVE.B D0,(A0)+
 CMP.L A0,A1
 BNE.S OMEMLP
 ADD.L D1,SP
 RTS


WRITEMEM:
 JSR READADR    * Speicherbereich verschieben
 MOVE.L D0,A0
 JSR READADR
 MOVE.L D0,A1
 CMP.L A1,A0
 BCC ERRO
 JSR READADR
 MOVE.L D0,A2
 CMP.L A2,A0
 BCC.S WOUTS   * A2 <= A0
 CMP.L A1,A2
 BCC.S WOUTS   * A2 => A1
 MOVE.L A1,D0
 SUB.L A0,D0
 ADD.L D0,A2
WRILP:
 MOVE.B -(A1),-(A2)
 CMP.L A0,A1
 BNE.S WRILP
 RTS
WOUTS:
 MOVE.B (A0)+,(A2)+
 CMP.L A0,A1
 BNE.S WOUTS
 RTS

COMPARE:
 JSR READADR    * Speicherber. vergl.
 MOVE.L D0,A0
 JSR READADR
 MOVE.L D0,A1
MCOMPLP:
 CMPM.B (A0)+,(A1)+  *<<<
 BEQ.S MCOMPLP
 SUBQ.L #1,A0
 MOVE.L A0,-(SP)
 JSR EINGPOS
 JSR RESTZLSH
 JSR SPACES
 MOVE.L (SP)+,D0
 JMP ADRHEX 


FINDBYTES:
 JSR MUSTREAD
 CMP.B #'"',D0  * in Disass.listing suchen ?
 BEQ DISFIND

 JSR READBACK
 JSR READADR  * Bytefolge im Ber. a0,a1 suchen
 MOVE.L D0,A0
 JSR READADR
 MOVE.L D0,A1
 CMP.L A1,A0
 BCC ERRO
 JSR READCHAR
 BEQ ERRO
 JSR READBACK
 CLR.L D1
GFBYLP:
 JSR READCHAR
 BEQ.S GFXT
 CMP.B #32,D0
 BEQ.S GFBYLP
 CMP.B #'"',D0
 BEQ.S FTEXT
 CMP.B #$2A,D0  * '*'= Joker
 BEQ.S FJOKER
 JSR READBACK
 JSR READBYTE   * Hexwert
FPUSHBY:
 MOVE.W D0,-(SP)
 ADDQ.W #2,D1
 BRA.S GFBYLP
FTEXT:
 JSR READCHAR
 BEQ.S GFXT
 CMP.B #$22,D0
 BEQ.S GFBYLP
 MOVE.W D0,-(SP)
 ADDQ.W #2,D1
 BRA.S FTEXT
FJOKER:
 MOVE.W #$8000,D0
 BRA.S FPUSHBY
GFXT:
 TST.W D1
 BEQ ERRO
 JSR CARRET
 CLR.L D3
 MOVE.W D1,D2
FINDLP:
 MOVE.W -2(A7,D2.W),D0   * Suchschleife
 BMI.S FJOK
 CMP.B (A0)+,D0
 BEQ.S FSTM
 TST.W D3
 BNE.S FNINIT
FWEITR:
 CMP.L A1,A0
 BCS.S FINDLP
FEND:
 ADD.L D1,SP
 RTS
FNINIT:
 SUB.L D3,A0
 MOVE.W D1,D2
 CLR.L D3
 BRA.S FWEITR
FJOK:
 ADDQ.L #1,A0
FSTM:
 ADDQ.W #1,D3
 SUBQ.W #2,D2
 BNE.S FINDLP

 MOVE.L A0,D0
 SUB.L D3,D0
 MOVE.W CRSX,D2    * max. 6 Zahlen/Zl.
 CMP.B #60,D2
 BNE.S FADR
 JSR LINEOUT
FADR:
 JSR SPACES
 JSR ADRHEX
 MOVE.L D0,A0
 ADDQ.L #1,A0
 JSR STOPTEST
 BEQ.S FEND
 CMP.B #32,D0
 BNE.S FANWT
 JSR CONIN
 CMP.B #$1B,D0
 BEQ.S FEND
FANWT:
 MOVE.W D1,D2
 CLR.L D3
 BRA.S FWEITR


DISFIND:
 LEA VRAM+2000,A0   * in Disass.listing suchen
 MOVE.L A0,A2
GETDFSTR:
 JSR READCHAR
 BEQ ERRO
 CMP.B #'"',D0
 BEQ.S DFSTRFU
 MOVE.B D0,(A0)+
 BRA.S GETDFSTR
DFSTRFU:
 CLR.B (A0)
 JSR READADR
 MOVE.L D0,A0
 LEA $FF8000,A1
 JSR READCHAR
 BEQ.S DFONEAD
 JSR READBACK
 JSR READADR
 MOVE.L D0,A1
DFONEAD:
 JSR CARRET
DISFNDLP:
 JSR DISASS
 LEA VRAM,A3
 MOVEQ #80,D0
 MULU CRSY,D0
 ADD.W D0,A3
 CLR.W D1
 MOVEQ #36,D2
DISSUCH:
 MOVE.B 0(A2,D1.W),D0
 BEQ.S DFOUND
 CMP.B 0(A3,D2.W),D0  * Text in VRAM suchen
 BEQ.S DF100
