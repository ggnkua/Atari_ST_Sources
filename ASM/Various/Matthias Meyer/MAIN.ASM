GOPRG:
 JSR READPC

GOPC:
 MOVE.L SUPVSTP,SP   * Programm starten
 MOVE.L USERSTP,A0 * Stackpointer init.
 MOVE A0,USP
 LEA REGIST,A6 * D0-D7/A0-A6 aus Puffer holen
 MOVE.L 56(A6),-(SP)
 MOVEM.L (A6),D0-D7/A0-A5 
 MOVE.L (SP)+,A6
 MOVE.L PCOUNTER,-(SP)
 MOVE.W PSTAT,-(SP)
 RTE * alter Trick


* Einsprung von EXCEPTION-Vektoren

BREAK:
 JSR SAVEREGS
 BSR.S GETPC * oberstes Byte des PC enthält Vektornummer (raffiniert!)
 BRA.S EL100
GETPC:
 MOVE.L (SP)+,D0
 MOVE.L D0,D1
 AND.L #$00FFFFFE,D1 * oberstes Byte des PC wieder löschen (kosmetisch)
 MOVE.L D1,-(SP)
 RTS
EL100:
 ROL.L #8,D0
 AND.W #$00FF,D0
 MOVE.W D0,D5  * Vektornummer

 CMP.W #4,D5
 BCC.S EXCNRM     * Exc. Gruppe Null
 MOVE.W (SP)+,D2 * Zusatzwort
 MOVE.L (SP)+,D3 * Zugriffsadr.
 MOVE.W (SP)+,D4 * Befehlsreg.
EXCNRM:
 JSR SAVECREGS

CLRTSTPUF:
 MOVE.W #$0B,-(SP) * Tastaturpuffer löschen
 TRAP #1 * CONSTAT
 ADDQ.L #2,SP
 TST.W D0
 BEQ.S TSTLEER
 JSR CONIN
 BRA.S CLRTSTPUF
TSTLEER:
 CLR.L D1         * Cu. nach unten
 MOVEQ #24,D2
 JSR RCRSET
 MOVEQ #'!',D0
 JSR PRINT
 JSR CRSLEFT      * blinkendes Ausrufezeichen
 JSR CRSON

 MOVE.W #$2300,SR * Interrupts für Tastaturbehandlung freigeben!
 JSR CONIN        * auf Tastendruck warten wegen Bildschirminhalt

 MOVE.W D0,D7

 LEA EXCMELD,A0
 JSR STROUT
 MOVE.W D5,D0
 EXT.L D0
 JSR DEZOUT
 JSR STROUT
 JSR BYTEHEX
 JSR STROUT
 LSL.W #2,D0
 JSR WORDHEX

 CMP.W #4,D5
 BCC.S EGRP12
 JSR STROUT
 MOVE.W D2,D0
 JSR BYTEBIN    * Zusatzwort binär
 JSR SPACES
 MOVE.L D3,D0
 JSR LONGHEX
 JSR SPACES
 MOVE.W D4,D0
 JSR WORDHEX
EGRP12:
 CMP.W #$1B,D7  * ESC
 BEQ MAINLOOP
 JSR REGISTS
 JMP MAINLOOP


SAVEREGS:
 MOVE.L A6,-(SP) * D0-D7/A0-A6 retten
 LEA REGIST,A6
 MOVEM.L D0-D7/A0-A5,(A6)
 MOVE.L (SP)+,56(A6)
 RTS

SAVECREGS:
 MOVE.L (SP)+,D0
 LEA PSTAT,A1
 MOVE.W (SP)+,(A1)+  * STATUS
 MOVE.L (SP)+,(A1)+  * PC
 MOVE USP,A2
 MOVE.L A2,(A1)+ * USP
 MOVE.L SP,(A1)  * SSP
 MOVE.L D0,-(SP)
 RTS

SAVREG:
 BSR.S SAVEREGS  * sämtliche Reg. retten
 BRA.S SAVECREGS


EXCMELD:
 DC.B '! exception no. ',0,'  (=$',0,')  adr. $',0
 DC.B CR,'additional  access      command',CR,0
 DC.W 0

READPC:
 JSR READCHAR
 BEQ.S RPNOADR
 JSR READBACK
 JSR READADR
 BTST #0,D0
 BNE ERRO
 LEA PCOUNTER,A0
 MOVE.L D0,(A0)
RPNOADR:
 RTS


EXCVSET:
 LEA CHGVECTS,A0  * Setzen der Exceptionvektoren
 LEA BREAK,A1    * auf BREAK-Entry richten 
EXVLP:
 CLR.L D0
 MOVE.B (A0)+,D0
 BEQ ILL
 MOVE.L D0,D1
 LSL.L #2,D0
 ROR.L #8,D1
 MOVE.L D0,A2
 MOVE.L A1,D2
 AND.L #$00FFFFFF,D2
 OR.L D1,D2
 MOVE.L D2,(A2)
 BRA.S EXVLP 



VECTORS:
 JSR CARRET * Exc.vektoren ausg.
 LEA BREAK,A1
 MOVEQ #8,D1
VECTLP:
 JSR ONESPACE
 MOVEQ #':',D0
 JSR PRINT
 MOVE.L D1,D0
 JSR ADRHEX
 MOVEQ #16,D0
 JSR SPACETAB
 MOVE.L D1,A0
 MOVE.L (A0),D0
 MOVE.L D0,D2
 AND.L #$FFFFFF,D2
 ROL.L #8,D0
 JSR BYTEHEX
 JSR TWOSPACE
 MOVE.L D2,D0
 JSR ADRHEX
 MOVEQ #32,D0
 CMP.L A1,D2
 BNE.S VCNOB
 MOVEQ #'*',D0
VCNOB:
 JSR PRINT
 JSR SPACES
 JSR DOPPELKR
 MOVE.L D1,D0
 LSR.L #2,D0
 JSR DEZOUT
 JSR LINEOUT
 JSR STOPTEST
 BEQ CRSUP
 ADDQ.L #4,D1
 CMP.W #$400,D1
 BNE.S VECTLP
 JMP CRSUP

