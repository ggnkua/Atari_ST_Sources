MEMORY:
 LEA MEMOUT,A3    * Hexdump
 MOVEQ #16,D3  * 16 Bytes pro Zeile
 BRA.S MEMDUMP

KONTROLL:
 LEA KONTROUT,A3  * Textkontrolle
 MOVEQ #64,D3
 BRA.S MEMDUMP

DISASSEM:
 LEA DISASS,A3    * Disassembler
 CLR.L D3

MEMDUMP:
 JSR READADR
 LEA $01000000,A1 * Default
 MOVE.L D0,A0
 JSR READCHAR
 BNE.S MZWAD
MEINZEL:
 JSR CARRET
 JSR (A3)
 JSR PRINTLINE
 ADD.L D3,A0
 JSR CONIN
 CMP.B #$1B,D0
 BEQ.S MEMEX
 CMP.B #32,D0
 BEQ.S MEINZEL
MEMSHLP:
 JSR CARRET
 JSR (A3)
 JSR PRINTLINE
 ADD.L D3,A0
 JSR STOPTEST
 BEQ.S MEMEX
 CMP.B #32,D0
 BEQ.S MHALT
MWEIT:
 CMP.L A1,A0 * a0 => a1 ?
 BCS.S MEMSHLP
MEMEX:
 RTS
MZWAD:
 JSR READADR
 MOVE.L D0,A1
 BRA.S MEMSHLP
MHALT:
 JSR CONIN
 CMP.B #$1B,D0
 BEQ.S MEMEX
 CMP.B #32,D0
 BEQ.S MEINZEL
 BRA.S MWEIT


MEMOUT:
 MOVEM.L D0-D1/A0,-(SP) * Ausgabe von 16 Speicherzellen hex. & ascii ab A0
 JSR ZEILANF
 JSR RESTZLSH
 JSR PUNKT
 MOVEQ #':',D0
 JSR PRINT
 MOVE.L A0,D0
 CMP.L #$FF8000,D0 * I/O Bereich?
 BCC.S IOMEM * ja
 JSR ADRHEX
 JSR SPACES
 MOVEQ #15,D1 * 16 Bytes
MEMOLP:
 CMP.L #$FF8000,A0 * I/O Bereich?
 BCC.S MEMXT * ja
 MOVE.B (A0)+,D0
 JSR BYTEHEX
 JSR ONESPACE
 DBF D1,MEMOLP
 JSR TWOSPACE
 SUB.L #16,A0
 MOVEQ #15,D1
MEMASC:
 MOVE.B (A0)+,D0
 JSR NCPRINT
 DBF D1,MEMASC
MEMXT:
 MOVEM.L (SP)+,D0-D1/A0
 RTS
IOMEM:
 BCLR #0,D0
 MOVE.L D0,A0
 JSR ADRHEX
 JSR SPACES
 MOVEQ #7,D1 * 8 WORDS
IOMLP:
 MOVE.W (A0)+,D0
 JSR WORDHEX
 MOVEQ #32,D0
 JSR PRINT
 JSR PRINT
 DBF D1,IOMLP
 BRA.S MEMXT

KONTROUT:
 MOVEM.L D0-D1/A0,-(SP) * Textauszug (64 Bytes) ab a0
 JSR ZEILANF
 JSR RESTZLSH
 JSR PUNKT
 MOVEQ #$27,D0 * '
 JSR PRINT
 MOVE.L A0,D0
 JSR ADRHEX
 JSR SPACES
 MOVEQ #63,D1
KTROLP:
 MOVE.B (A0)+,D0
 JSR NCPRINT
 DBF D1,KTROLP
 MOVEM.L (SP)+,D0-D1/A0
 RTS

