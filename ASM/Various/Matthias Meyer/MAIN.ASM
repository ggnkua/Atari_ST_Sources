FLOAD:
 JSR GETFLNAM   * a1= Filename,a2= DTA
 JSR READADR
 MOVE.L D0,A3   * Ladeadresse
 CLR.W -(SP)
 PEA (A1)
 MOVE.W #$4E,-(SP)  * SFIRST
 TRAP #1
 ADDQ.L #8,SP
 TST.W D0
 BNE DISKERR

 LEA LOADINFO,A0
 JSR STROUT
 JSR FILENOUT
 MOVE.L 26(A2),D2   * Anzahl Bytes
 MOVE.L D2,D3
 ADD.L A3,D3        * => Endadr.

 CLR.W -(SP)        * nur lesen
 PEA 30(A2)         * filename adr.
 MOVE.W #$3D,-(SP)  * OPEN
 TRAP #1
 ADDQ.L #8,SP
 TST.W D0
 BMI DISKERR
 MOVE.W D0,D1       * HANDLE-Nr.

 PEA (A3)           * BUFFER
 MOVE.L D2,-(SP)    * AMOUNT
 MOVE.W D1,-(SP)    * HANDLE
 MOVE.W #$3F,-(SP)  * READ
 TRAP #1       
 ADD.W #12,SP
 TST.L D0
 BMI DISKERR
 JSR CLOSED1
 BMI DISKERR
 LEA FEADRTX,A0
 JSR STROUT
 MOVE.L D3,D0
 JMP ADRHEX


