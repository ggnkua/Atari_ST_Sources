 RTS

EINGPOS:
 MOVEM.L D1-D2,-(SP) * C. auf 1. Zch. nach gelesenem
 MOVE.W EINGPTR,D1
 MOVE.W CRSY,D2
 JSR RCRSET
 MOVEM.L (SP)+,D1-D2
 RTS

RMINUS:
 JSR READLONG
 MOVE.L D0,D1
