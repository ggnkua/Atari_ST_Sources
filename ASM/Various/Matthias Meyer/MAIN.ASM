 JSR MUSTREAD
 CMP.B #'+',D0
 BEQ.S RPLUS
 CMP.B #'-',D0
 BEQ RMINUS
 CMP.B #'*',D0
 BEQ RMULT
 CMP.B #'/',D0
 BEQ RDIV
 CMP.B #'&',D0
 BEQ RUND
 CMP.B #'▌',D0
 BEQ RODER
 CMP.B #'!',D0
