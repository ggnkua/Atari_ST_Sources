 JSR WORDHEX
 MOVEQ #32,D0
 JSR PRINT
 JSR PRINT
 MOVEQ #'\',D0
 JSR PRINT
 MOVE.W (SP)+,D0
 JMP WORDHEX  * Div. rest ausg.

