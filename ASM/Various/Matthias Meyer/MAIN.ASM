CR      EQU $0D


M:
START:
 NOP

 MOVE.L 4(SP),A0   * Programm anmelden (RAM-Bereich schützen)
 MOVE.L 12(A0),D0
 ADD.L 20(A0),D0
 ADD.L 28(A0),D0
 ADD.L #2080+$100,D0
 MOVE.L D0,-(SP)   * Länge des Programmbereichs
 PEA (A0)
 CLR.W -(SP)
 MOVE.W #$4A,-(SP) * SETBLOCK
 TRAP #1
 ADD.W #12,SP

 CLR.L -(SP)       * Supervisormode ein
 MOVE.W #$20,-(SP)
 TRAP #1           * a7' nach d0, a7 nach a7'
 ADDQ.L #6,SP
 LEA USERSTP,A0
 MOVE.L SP,(A0)+
 MOVE.L SP,USP
 MOVE.L D0,(A0)
 MOVE.L D0,SP

 JSR SCRCLR      * Bildschirm löschen, VRAM init.
 MOVEQ #'v',D0   * Überlauf ein
 JSR ESCOUT
 LEA STMELD,A0   * Startmeldung
 JSR STROUT
 LEA START,A1
 MOVE.L A1,D0
 JSR ADRHEX
 JSR STROUT
 LEA VRAM+2080,A0
 MOVE.L A0,D0
 JSR ADRHEX      * Prog.ende
 JSR CARRET

 JSR EXCVSET     * Exceptions auffangen

 MOVEQ #14,D0 * 8+7 -1
 LEA REGIST,A0
REGCLR:
 CLR.L (A0)+     * Registerspeicher löschen
 DBF D0,REGCLR
 CLR.W (A0)+     * PSTATUS
 LEA START,A1
 MOVE.L A1,(A0)  * PC auf Neustart


MAINLOOP:
 MOVE.W #$2300,SR
 JSR CARRET      * Eingabeschleife
 JSR PUNKT
NOPNTINP:
 JSR INPLINE     * Eing. abwarten
MUEBERLES:
 JSR READCHAR    * erstes Zeichen der Zeile nach D0
 BEQ.S MAINLOOP  * Leerzeile
 CMP.B #$20,D0
 BEQ.S MUEBERLES
 CMP.B #'.',D0
 BEQ.S MUEBERLES
 JSR KLEINERBU   * Gro×buchstaben in kleine umwandeln
 LEA CMDBYTES,A0
 CLR.W D1
CMDSUCH:
 MOVE.B (A0)+,D2
 BEQ.S ERRO      * kein erkennbarer Befehl
 CMP.B D2,D0
 BEQ.S CMDFND
 ADD.W #1,D1
 BRA.S CMDSUCH
CMDFND:
 LEA CMDADRESS,A0
 LEA START,A1
 LSL.W #1,D1         * d1 mal zwei
 ADD.W 0(A0,D1.W),A1 * Adressdistanz zum Start holen
* und Startadr. des Prog. dazuaddieren
 JSR (A1)            * Befehl ausführen
 BRA.S MAINLOOP      * und nächste Eingabe holen

*

ERRO:
 MOVE.L SUPVSTP,SP * Stackpointer nach Fehler init.
 JSR EINGPOS
 MOVEQ #'?',D0
 JSR PRINT
 BRA.S MAINLOOP




FRSTFLAG:
 DC.W 0   * wenn 0: Programm wird mit KEEP PROCESS verlassen
* sonst einfacher TERM

PRINTNR:
 DC.W 0       * 0-nur Screen, 1-Centronics, 2- RS 232

ZWA: DC.L 0   * Zwischenspeicher

CHGVECTS:
* aufzufangende Exceptions
 DC.B 2,3,4,5,6,7,8,9,32,0,0,0,0,0,0


STMELD:
 DC.B ' *** Happy-Computer - 68000er   ***',13,10
 DC.B ' *** MonST v1.0 by M.Meyer 3/86 ***',CR,CR,'  $',0,' - $',0


* Befehlskürzel

CMDBYTES:
 DC.B '>rmkdg'
 DC.B ';:,',$27 * >'<
 DC.B 'vow=ft'
 DC.B '#$%?'
 DC.B 'ls@'
 DC.B 'zx'
 DC.B 0

 DC.W 0,0  * Platz für Erweiterungen durch den Anwender

* Adre×distanzen zum ProgrammSTART

CMDADRESS:
 DC.W OUTSET-M,REGISTS-M,MEMORY-M,KONTROLL-M,DISASSEM-M,GOPRG-M

 DC.W REGMOD-M,MEMMOD-M,DISASSMOD-M,KONTMOD-M

 DC.W VECTORS-M,OCCUPY-M,WRITEMEM-M,COMPARE-M,FINDBYTES-M,TRACER-M

 DC.W DEZUM-M,HEXUM-M,BINUM-M,RECHNUNG-M

 DC.W FLOAD-M,FSAVE-M,DIRECT-M

 DC.W DISKMON-M,EXIT-M

 DC.W 0,0,0



* Es folgen die einzelnen Befehlsfunktionen


OUTSET:
 JSR MUSTREAD  * Ausgabeeinheit setzen
 CMP.B #'0',D0 * 0...2
 BCS ERRO
 CMP.B #'3',D0
 BCC ERRO
 SUB.B #'0',D0
 LEA PRINTNR,A0
 MOVE.W D0,(A0)
 RTS


REGISTS:
 JSR CARRET
REGISTOUT:
 LEA REGINFO,A0  * zwischengespeicherte Prozessorregister
 JSR STROUT      * ausgeben
 JSR LINEOUT     * CR + evtl. Drucker
 JSR PUNKT
 MOVEQ #';',D0
 JSR PRINT
 JSR ONESPACE
 JSR SPACES
 MOVE.L PCOUNTER,D0
 JSR LONGHEX
 JSR SPACES
 MOVE.W PSTAT,D1 * Statuswort binär ausg.
 MOVE.W D1,D0
 LSR.W #8,D0
 JSR BYTEBIN
 JSR ONESPACE
 MOVE.B D1,D0
 JSR BYTEBIN
 JSR LINEOUT
 JSR STROUT
 JSR LINEOUT
 LEA REGIST,A0
 MOVEQ #'0',D1
REG_LP:
 JSR PUNKT
 MOVEQ #';',D0
 JSR PRINT
 MOVE.B D1,D0
 JSR PRINT
 BSR.S SPACES
 MOVE.L (A0),D0   * Datenregister
 JSR LONGHEX
 CMP.B #'7',D1
 BEQ.S REXIT
 BSR.S SPACES
 MOVE.L 32(A0),D0 * Adre×reg.
 JSR LONGHEX
 ADDQ.L #4,A0
 JSR LINEOUT
 ADDQ.B #1,D1
 BRA.S REG_LP
REXIT:
 JSR LINEOUT
 LEA USRR,A0
 JSR STROUT
 MOVE.L USERSTP,D0
 JSR LONGHEX
 JSR LINEOUT
 JSR STROUT
 MOVE.L SUPVSTP,D0
 JSR LONGHEX
 JMP PRINTLINE

SPACES:
 MOVE.L D0,-(SP)
 JSR FOURSPACES
 MOVE.L (SP)+,D0
 RTS

REGINFO:
 DC.B '       PC:         T S  III    XNZVC',0
 DC.B '       data:       adress:',0
USRR:
 DC.B '.;USP  ',0,'.;SSP  ',0
 DC.W 0

