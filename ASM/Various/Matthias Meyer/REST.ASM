DISASS:
 MOVEM.L D0-D7/A1-A6,-(SP)  * ab (a0) disassemblieren
 MOVE.L A0,D0
 BCLR #0,D0
 MOVE.L D0,A1
 JSR ZEILANF
 JSR RESTZLSH
 JSR CLRWBUF
 JSR PUNKT
 JSR KOMMA
 MOVE.L A1,D0
 JSR ADRHEX
 JSR TWOSPACE
 MOVE.W (A1)+,D1 * Befehlswort holen
 MOVEQ #1,D7     * Default Wortlänge
 MOVE.W D1,D0
 JSR WORDHEX
 MOVEQ #36,D0
 JSR SPACETAB
 MOVE.W D1,D0
 AND.W #$F000,D0
 ROL.W #5,D0      * oberstes Nibble * 2
 LEA M,A0
 ADD.W DHINIBBS(PC,D0.W),A0
 JSR (A0)
 BRA.S DEXIT
DHINIBBS:
 DC.W DNIBB0-M,DMOVEB-M,DMOVEL-M,DMOVEW-M,DNIBB4-M,DNIBB5-M,DNIBB6-M,DNIBB7-M
 DC.W DNIBB8-M,DNIBB9-M,DILLEG-M,DNIBBB-M,DNIBBC-M,DNIBBD-M,DNIBBE-M,DILLEG-M

DEXIT:
 MOVEQ #15,D1
 MOVE.W CRSY,D2
 JSR RCRSET
 MOVE.W WBUFPTR,D1 * zum Opcode gehörende Worte ausg.
 BEQ.S DRESTA0
 MOVEQ #2,D2
DWBUFPRT:
 MOVE.W WBUFPTR(PC,D2.W),D0
 JSR WORDHEX
 JSR ONESPACE
 ADDQ.W #2,D2
 SUBQ.W #2,D1
 BNE.S DWBUFPRT
DRESTA0:
 MOVE.L A1,A0    * Zeiger auf nächsten Befehl
 MOVEM.L (SP)+,D0-D7/A1-A6
 RTS

CLRWBUF:
 LEA WBUFPTR,A0
 CLR.W (A0)
 RTS
WBUFPTR: DC.W 0   * Anzahl Worte im Buffer MAL 2
 DS.W 4

WINBUF:
 LEA WBUFPTR,A0   * Wort in d0 in Zwischenspeicher
 ADDQ.W #2,(A0)
 ADD.W (A0),A0
 MOVE.W D0,(A0) *
 RTS


