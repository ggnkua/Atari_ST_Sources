LINEOUT:
 BSR.S PRINTLINE
 JMP CARRET

PRINTLINE:
 MOVEM.L D0-D2/A0-A1,-(SP) * aktuelle Zeile (auf Drucker) ausg.
 MOVE.W PRINTNR,D1
 BEQ.S PRLXT
 SUBQ.W #1,D1
 EOR.W #1,D1
 ADDQ.W #4,D1
 MOVEQ #80,D0
 MULU CRSY,D0
 LEA VRAM,A1
 ADD.W D0,A1
 MOVEQ #78,D2
PRLNLP:
 MOVE.B (A1)+,D0
 BSR.S BOUTP
 TST.W D0
 BEQ.S PRLXT
 DBF D2,PRLNLP
 MOVEQ #CR,D0
 BSR.S BOUTP
 MOVEQ #10,D0
 BSR.S BOUTP
PRLXT:
 MOVEM.L (SP)+,D0-D2/A0-A1
 RTS

BOUTP:
 AND.W #$FF,D0
 MOVE.W D0,-(SP)
 MOVE.W D1,-(SP)
 TRAP #1    * OUTPUT
 ADDQ.L #4,SP
 RTS

*

NCPRINT:
 CMP.B #32,D0
 BCC.S PRINT * kein Controlzeichen
 MOVEQ #'.',D0

PRINT:
 MOVEM.L D0-D2/A0-A2,-(SP) * Ausgabe eines Zeichens in D0 <<<
 CMP.B #CR,D0
 BEQ.S ZCR
 CMP.B #32,D0 * keine anderen Steuerzeichen
 BCS.S EPR
 JSR CONOUT
 LEA VRAM,A0
 MOVEQ #80,D1
 LEA CRSY,A1  * plus Cursorzeile mal 80
 MOVE.W (A1),D2
 MULU D2,D1
 ADD.L D1,A0
 LEA CRSX,A1  * plus Cursorspalte
 CLR.L D2
 MOVE.W (A1),D2
 ADD.L D2,A0
 MOVE.B D0,(A0)   * und Zeichen ins VRAM
 MOVE.W D2,D0     * CRSX
 CMP.B #79,D0
 BEQ.S NLINE * letzte Spalte
 ADDQ.W #1,D0
 MOVE.W D0,(A1)
EPR:
 MOVEM.L (SP)+,D0-D2/A0-A2
 RTS
NLINE:
 CLR.W (A1)+
 CMP.W #24,(A1) * CRSY
 BEQ.S UPSCROLL * untere rechte Ecke
 ADDQ.W #1,(A1)
 BRA.S EPR
ZCR:
 JSR CONOUT
 MOVEQ #10,D0 * LINEFEED
 JSR CONOUT
 LEA CRSX,A0
 CLR.W (A0)+  * Spalte =0
 CMP.W #24,(A0) * letzte Zeile ?
 BEQ.S UPSCROLL
 ADDQ.W #1,(A0)
 BRA.S EPR
UPSCROLL:
 CLR.L D0
 JSR DELLINE
 BRA.S EPR

