STROUT:
 MOVE.L D0,-(SP) * String nach (a0) ausg. <<<
STRLP:
 MOVE.B (A0)+,D0
 BEQ.S STREND
 JSR PRINT
 BRA.S STRLP
STREND:
 MOVE.L (SP)+,D0
 RTS

CRSON:
 MOVE.L D0,-(SP) * Cursorblinken ein
 MOVEQ #'e',D0
 BSR.S ESCOUT
 MOVE.L (SP)+,D0
 RTS

CRSOFF:
 MOVE.L D0,-(SP) * Cursorblinken aus
 MOVEQ #'f',D0
 BSR.S ESCOUT
 MOVE.L (SP)+,D0
 RTS

ESCOUT:
 MOVE.L D0,-(SP)
 MOVEQ #27,D0  * escape
 BSR.S CONOUT
 MOVE.L (SP)+,D0
 BRA.S CONOUT

RAWCONIN:
 MOVE.L A0,-(SP)         * D0:=INKEY$
 MOVE.L #$000600FF,-(SP) * Trick
 TRAP #1
 ADDQ.L #4,SP
 MOVE.L (SP)+,A0
 RTS

CONOUT:
 MOVEM.L D0/A0,-(SP)
 AND.W #$00FF,D0
 MOVE.W D0,-(SP)
 MOVE.W #2,-(SP)
 TRAP #1
 ADDQ.L #4,SP
 MOVEM.L (SP)+,D0/A0
 RTS

CONIN:
 MOVE.L A0,-(SP) * direct con in without echo
 MOVE.W #7,-(SP)
 TRAP #1    * Zeichen von Tastatur holen
 ADDQ.L #2,SP
 MOVE.L (SP)+,A0
 RTS

