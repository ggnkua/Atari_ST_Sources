 CMP.L A1,A0
 BNE.S LINS

CLRLINE:
 MOVEM.L D0-D2/A0,-(SP) * Zeile D0 im VRAM löschen
 LEA VRAM,A0
 MOVEQ #80,D1
 MULU D0,D1
 ADD.L D1,A0
 MOVEQ #19,D2
 MOVE.L #$20202020,D1
LICL:
 MOVE.L D1,(A0)+
 DBF D2,LICL
 MOVEM.L (SP)+,D0-D2/A0
ILL:
 RTS

CBACKSPC:
 LEA VRAM,A0
 LEA CRSX,A1
 CMP.W #0,(A1)+
 BEQ.S ILL * Spalte 0
 MOVE.W (A1),D0 * CRSY
 MOVEQ #80,D1
 MULU D0,D1
 ADD.L D1,A0
 MOVE.W -(A1),D1 * CRSX
 SUBQ.W #1,(A1)
 ADD.L D1,A0
 MOVE.L A0,A1
 SUBQ.L #1,A0
BCKLP:
 MOVE.B (A1)+,(A0)+
 ADDQ.W #1,D1
 CMP.W #80,D1
 BNE.S BCKLP
 MOVE.B #32,(A0)
 MOVE.W CRSY,D0
 JMP RESTLN

CDELETE:
 LEA VRAM,A0
 LEA CRSY,A1
 MOVE.W (A1),D0
 MOVEQ #80,D1
 MULU D0,D1
 ADD.L D1,A0
 MOVE.W -(A1),D1 * CRSX
 ADD.L D1,A0
 MOVE.L A0,A1
 ADD.L #1,A1
CDELLP:
 MOVE.B (A1)+,(A0)+
 ADDQ.W #1,D1
 CMP.W #80,D1
 BNE.S CDELLP
 MOVE.B #32,-(A0)
 MOVE.W CRSY,D0
 JMP RESTLN

CINSERT:
 LEA VRAM,A0 * Space einfügen, Rest der Zeile nach rechts hinaus
 LEA CRSY,A1
 MOVE.W (A1),D0
 ADDQ.W #1,D0
 MOVEQ #80,D1
 MULU D0,D1
 ADD.L D1,A0
 MOVE.L A0,A2
 SUBQ.L #1,A0
 MOVE.W -(A1),D0 * CRSX
 MOVEQ #79,D1
 SUB.W D0,D1
CINSLP:
 MOVE.B -(A0),-(A2)
 DBF D1,CINSLP
 MOVE.B #32,(A2)
 MOVE.W CRSY,D0

RESTLN:
 JSR CRSOFF
 MOVEM.L D0-D2/A0,-(SP) * Zeile d0 neu ausg.
 MOVE.W D0,D2
 MOVEQ #'Y',D0
 JSR ESCOUT
 MOVE.W D2,D0
 ADD.W #32,D0
 JSR CONOUT
 MOVE.W #32,D0 * Spalte = 0
 JSR CONOUT
 LEA VRAM,A0
 MOVEQ #80,D1
 MULU D2,D1
 ADD.L D1,A0
 MOVEQ #79,D1
 MOVE.W CRSY,D0 * untere rechte Ecke kann nicht beschrieben werden!
 CMP.W #24,D0
 BNE.S RSTLP
 MOVEQ #78,D1
RSTLP:
 MOVE.B (A0)+,D0
 JSR CONOUT
 DBF D1,RSTLP
 JSR CRSET  * Cu. auf alte Pos.
 MOVEM.L (SP)+,D0-D2/A0
 RTS

RESTZLSH:
 MOVEM.L D0-D1/A0,-(SP) *  Rest d. Zl. ab C.pos. incl. löschen 
 MOVEQ #'K',D0
 JSR ESCOUT
 LEA VRAM,A0
 MOVE.W CRSY,D0
 MOVEQ #80,D1
 MULU D0,D1
 ADD.L D1,A0
 MOVE.W CRSX,D1
 ADD.L D1,A0
 MOVEQ #79,D0
 SUB.B D1,D0
 MOVEQ #32,D1
RLSCH:
 MOVE.B D1,(A0)+
 DBF D0,RLSCH
 MOVEM.L (SP)+,D0-D1/A0
 RTS

ZEILANF:
 MOVEM.L D0-D3/A0-A3,-(SP) * C. an Anfang der Zeile
 CLR.W D1
 MOVE.W CRSY,D2
 JSR RCRSET
 MOVEM.L (SP)+,D0-D3/A0-A3
 RTS

STOPTEST:
 JSR RAWCONIN
 CMP.B #$1B,D0 * ESC
 RTS

