; THIS IS THE PICTURE FADE ROUTINE
; IF YOU WISH TO CHANGE THE FADE METHOD THEN
; MODIFY THE SERIES OF ENTRIES AT THE END OF THIS LISTING SO THAT
; IT PROVIDES THE NEW FADE PATTERN
; CALL WITH A BSR FADEIT REQUIRES SCREEN ADDR TO BE IN A6

fadeit	movem.l	d0-d7/a0-a6,-(sp)
	clr.l	currentander
	move.l	#ander1,currentander

	move.l	#16,d6
; The number of different blocks to be 'anded' on to the screen-1
mainfader	clr.l	currentblock
	move.l	a6,a0


	move.l	#199,d0
fadeloop	bsr	anditon
	add.l	#1,currentblock
	move.l	currentblock,d7
	divu	#20,d7
	swap	d7
	tst.w	d7
	beq	special
	add.l	#8,a0
	
continue	dbf	d0,fadeloop

	add.l	#20*2,currentander

	dbf	d6,mainfader

	movem.l	(sp)+,d0-d7/a0-a6
	rts

special
	add.l	#8,a0
	add.l	#$a0*19,a0
	bra	continue

anditon
	movem.l	d2-d3/a0-a1,-(sp)
	move.l	#19,d2
	move.l	#0,currline
.ander
	move.l	currline,d3
	asl.w	#1,d3
	
	move.l	currentander,a1
	move.w	0(a1,d3.w),d3
	
	and.w	d3,(a0)
	and.w	d3,2(a0)
	and.w	d3,4(a0)
	and.w	d3,6(a0)
	
	add.l	#1,currline
	add.l	#$a0,a0
	dbf	d2,.ander

	movem.l	(sp)+,d2-d3/a0-a1
	rts

