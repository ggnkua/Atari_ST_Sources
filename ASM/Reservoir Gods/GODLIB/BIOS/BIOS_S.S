*------------------------------------------------------------------------------------*
* FUNCTION : Bios_Rwabs
* ACTION   : 
* CREATION : 11.11.01 PNK
*------------------------------------------------------------------------------------*

Bios_Rwabs:
	pea		(a2)

	lea		4(a7),a2
	move.l	(a2)+,-(a7)
	move.w	(a2)+,-(a7)
	move.w	d2,-(a7)
	move.w	d1,-(a7)
	pea		(a0)
	move.w	d0,-(a7)
	move.w	#4,-(a7)
	trap	#13
	lea		18(a7),a7

	move.l	(a7)+,a2
	rts


